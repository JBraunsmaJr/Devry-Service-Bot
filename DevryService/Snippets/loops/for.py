items = ["Meow", "Mix", "Finally", "Delivers", "Thank", "You", "Covid"]
"""
    Arrays are 0 based. So a for loop is excellent when iterating over them!

    To get the length of an array we can use len(arrayVariableHere)
"""

for x in range(len(items)):
    print(items[x])